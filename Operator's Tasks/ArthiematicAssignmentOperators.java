package javafundamentals;

public class ArthiematicAssignmentOperators {

public static void main(String[] args) {
    
    int num1 = 20;
    int num2 = 30;
    
    num1 += num2;
    System.out.println("Additional Assignment: " + num1);
    
    num1 -= num2;
    System.out.println("Subtractional Assignment: " + num1);
    
    num1 *= num2;
    System.out.println("Multiplicative Assignment: " + num1);
    
    num1 %= num2;
    System.out.println("Modulus Assignment: " + num1);
    
    
}    
}
